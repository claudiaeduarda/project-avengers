package com.github.claudiaeduarda.projectavengers.models;

public class Movies {

	private Integer id;
	private String nome;
	private String ano;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String nome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String Ano() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
}
